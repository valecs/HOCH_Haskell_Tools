      subroutine h2co_mrci(rij,en)
c
c     note h1 and h2 are not equivalent, it is assumed that
c     h2 is further away from the other atoms than h1
c
c     rij(1) = r(c-o) (au)
c     rij(2) = r(c-h1)
c     rij(3) = r(c-h2)
c     rij(4) = r(o-h1)
c     rij(5) = r(o-h2)
c     rij(6) = r(h1-h2)
c
c     en = energy (hartree)
c
c
      implicit real*8 (a-h,o-z)
      dimension rij(6),tij(6)
      data rech,reoh,rehh / 2.15d0 , 1.85d0, 1.45d0 /

      ru1 = 3.0d0
      rl1 = 2.0d0

      ru2 = 4.0d0
      rl2 = 3.0d0

      scha = rij(2)/rech
      schb = rij(3)/rech
      soha = rij(4)/reoh
      sohb = rij(5)/reoh
      shh  = rij(6)/rehh

      Ra=dmin1(scha,soha,shh)
      Rb=dmin1(schb,sohb,shh)
      S0=dmax1(Ra,Rb)

      if(Ra .gt. Rb) then
       tij(1) = rij(1)
       tij(2) = rij(3)
       tij(3) = rij(2)
       tij(4) = rij(5)
       tij(5) = rij(4)
       tij(6) = rij(6)
      else
       tij(1) = rij(1)
       tij(2) = rij(2)
       tij(3) = rij(3)
       tij(4) = rij(4)
       tij(5) = rij(5)
       tij(6) = rij(6)
      endif

      x1=(S0-rl1)/(ru1-rl1)
      x2=(S0-rl2)/(ru2-rl2)

      if (x1.LE.0.) then
        call f27367(tij,en)
      else if ((x1.GT.0.).and.(x1.LT.1.)) then
        call f27367(tij,v1)
        call f33757(tij,v2)
        s=1.0-(10.0*x1**3-15.0*x1**4+6.0*x1**5)
        en=v1*s+v2*(1-s)
      else if ((x2.GE.0.).and.(x2.LT.1.)) then
        call f33757(tij,v1)
        call f15158(tij,v2)
        s=1.0-(10.0*x2**3-15.0*x2**4+6.0*x2**5)
        en=v1*s+v2*(1-s)
      else if (x2.GE.1.) then
        call f15158(tij,en)
      endif

      return
      end

